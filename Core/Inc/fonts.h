/*
 * fonts.h — Шрифт 5×7 для OLED-дисплея SSD1306
 *
 * ЯК ВЛАШТОВАНИЙ ШРИФТ:
 *
 * Кожен символ — це прямокутник 5 пікселів шириною і 7 висотою.
 * Між символами додається 1 піксель відступу, тому фактична
 * ширина "клітинки" — 6 пікселів.
 *
 * Кожен символ описується П'ЯТЬМА БАЙТАМИ (по одному на стовпець).
 * Кожен байт — це вертикальний стовпець з 7 пікселів:
 *   біт 0 = верхній піксель
 *   біт 6 = нижній піксель
 *   біт 7 = не використовується (завжди 0)
 *
 * Приклад: символ 'A' (0x41)
 *
 *   Стовпці:  [0]   [1]   [2]   [3]   [4]
 *   Hex:      0x7E  0x09  0x09  0x09  0x7E
 *   Binary:
 *     bit 0:   0     1     1     1     0     ← верх
 *     bit 1:   1     0     0     0     1
 *     bit 2:   1     0     0     0     1
 *     bit 3:   1     1     1     1     1     ← горизонтальна риска
 *     bit 4:   1     0     0     0     1
 *     bit 5:   1     0     0     0     1
 *     bit 6:   1     0     0     0     1     ← низ
 *
 *   Візуально:
 *     .###.
 *     #...#
 *     #...#
 *     #####
 *     #...#
 *     #...#
 *     #...#
 *
 * Масив починається з символу пробіл (ASCII 0x20 = 32).
 * Індекс символу в масиві: (ascii_code - 32) * 5.
 *
 * Скільки пам'яті займає: 95 символів × 5 байт = 475 байт.
 * Для STM32F411 з 512 КБ Flash — мізерно.
 */

#ifndef FONTS_H
#define FONTS_H

#include <stdint.h>

/* Розміри шрифту */
#define FONT_WIDTH      5       /* Ширина символу в пікселях */
#define FONT_HEIGHT     7       /* Висота символу в пікселях */
#define FONT_SPACING    1       /* Відступ між символами */
#define FONT_CHAR_W     (FONT_WIDTH + FONT_SPACING)  /* Повна ширина клітинки = 6 */

/* Перший та останній символ у масиві */
#define FONT_FIRST_CHAR 0x20    /* Пробіл (ASCII 32) */
#define FONT_LAST_CHAR  0x7E    /* Тильда '~' (ASCII 126) */

/* Масив шрифту (визначений у fonts.c) */
extern const uint8_t Font5x7[];

#endif /* FONTS_H */
