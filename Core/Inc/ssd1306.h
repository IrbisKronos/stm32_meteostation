/*
 * ssd1306.h — Заголовний файл драйвера OLED-дисплея SSD1306
 *
 * SSD1306 — це контролер монохромного OLED-дисплея 128×64 пікселів.
 * Кожен піксель може бути або увімкнений (білий/жовтий/синій),
 * або вимкнений (чорний). Колір залежить від типу OLED-панелі.
 *
 * Твій дисплей "синьо-жовтий" — верхні 16 рядків жовті,
 * нижні 48 — сині. Це апаратна особливість панелі,
 * програмно це не змінити.
 */

#ifndef SSD1306_H
#define SSD1306_H

#include "stm32f4xx_hal.h"

/* ============================================================
 * КОНСТАНТИ ДИСПЛЕЯ
 * ============================================================ */

/* Розміри дисплея в пікселях */
#define SSD1306_WIDTH           128
#define SSD1306_HEIGHT          64

/*
 * I2C-адреса SSD1306 (7-бітна).
 * Стандартна для більшості модулів — 0x3C.
 * Деякі модулі мають 0x3D (залежить від резистора на платі).
 * HAL вимагає зсунуту (8-бітну) адресу: 0x3C << 1 = 0x78.
 */
#define SSD1306_I2C_ADDR        (0x3C << 1)

/*
 * ОРГАНІЗАЦІЯ ПАМ'ЯТІ ДИСПЛЕЯ (GDDRAM):
 *
 * Пам'ять SSD1306 організована як 8 "сторінок" (pages) по 128 стовпців.
 * Кожен байт у пам'яті відповідає за 8 вертикальних пікселів (один стовпець
 * в межах однієї сторінки).
 *
 *   Сторінка 0: рядки 0–7     (8 пікселів висоти)
 *   Сторінка 1: рядки 8–15
 *   Сторінка 2: рядки 16–23
 *   ...
 *   Сторінка 7: рядки 56–63
 *
 * Всього: 8 сторінок × 128 стовпців = 1024 байти.
 *
 * Біт 0 байта = верхній піксель, біт 7 = нижній піксель.
 *
 * Приклад: записати байт 0b00000001 в стовпець 10, сторінку 0
 *          → загориться піксель (10, 0).
 *          Записати 0b10000000 → загориться піксель (10, 7).
 *
 * Ми працюємо через ЕКРАННИЙ БУФЕР (framebuffer):
 * 1. Малюємо все в RAM-буфер (масив 1024 байти).
 * 2. Одним burst-write відправляємо весь буфер на дисплей.
 *
 * Це набагато ефективніше, ніж відправляти кожен піксель окремо.
 */
#define SSD1306_BUFFER_SIZE     (SSD1306_WIDTH * SSD1306_HEIGHT / 8)

/* Кольори (для монохромного дисплея — увімкнено/вимкнено) */
typedef enum {
    SSD1306_COLOR_BLACK = 0,    /* Піксель вимкнений */
    SSD1306_COLOR_WHITE = 1     /* Піксель увімкнений */
} SSD1306_Color;

/* Структура драйвера */
typedef struct {
    I2C_HandleTypeDef *hi2c;                    /* I2C handle */
    uint8_t buffer[SSD1306_BUFFER_SIZE];        /* Екранний буфер (1024 байти) */
    uint16_t cursor_x;                          /* Поточна X-позиція курсору */
    uint16_t cursor_y;                          /* Поточна Y-позиція курсору */
} SSD1306_Handle;

/* ============================================================
 * ПУБЛІЧНІ ФУНКЦІЇ (API)
 * ============================================================ */

/* Ініціалізація дисплея: послідовність команд із даташита */
HAL_StatusTypeDef SSD1306_Init(SSD1306_Handle *dev, I2C_HandleTypeDef *hi2c);

/* Відправити екранний буфер на дисплей (оновити зображення) */
HAL_StatusTypeDef SSD1306_UpdateScreen(SSD1306_Handle *dev);

/* Очистити екранний буфер (заповнити чорним) */
void SSD1306_Clear(SSD1306_Handle *dev);

/* Заповнити екранний буфер (заповнити білим) */
void SSD1306_Fill(SSD1306_Handle *dev);

/* Встановити один піксель у буфері */
void SSD1306_DrawPixel(SSD1306_Handle *dev, uint16_t x, uint16_t y, SSD1306_Color color);

/* Встановити позицію курсору для тексту */
void SSD1306_SetCursor(SSD1306_Handle *dev, uint16_t x, uint16_t y);

/* Вивести один символ (ASCII) у позицію курсору */
void SSD1306_WriteChar(SSD1306_Handle *dev, char ch, SSD1306_Color color);

/* Вивести рядок тексту (ASCII) починаючи з курсору */
void SSD1306_WriteString(SSD1306_Handle *dev, const char *str, SSD1306_Color color);

/* Намалювати горизонтальну лінію */
void SSD1306_DrawHLine(SSD1306_Handle *dev, uint16_t x, uint16_t y,
                       uint16_t width, SSD1306_Color color);

#endif /* SSD1306_H */
