/*
 * bme280.h — Заголовний файл драйвера BME280
 *
 * Цей файл описує "контракт" — які функції та структури доступні
 * іншим файлам проєкту. Сам код реалізації — в bme280.c.
 */

#ifndef BME280_H
#define BME280_H

#include "stm32f4xx_hal.h"  // Для типів HAL (I2C_HandleTypeDef тощо)

/* ============================================================
 * АДРЕСИ ТА КОНСТАНТИ
 * ============================================================
 * Вся інформація нижче взята з даташита BME280 (Bosch).
 * Даташит — це "паспорт" мікросхеми, де описано кожен регістр,
 * кожен біт і кожну формулу. Без даташита embedded не існує.
 */

/* I2C-адреса BME280 (7-бітна).
 * Якщо пін SDO на модулі притягнутий до GND → 0x76.
 * Якщо SDO притягнутий до VCC → 0x77.
 * HAL вимагає зсунуту адресу (8-бітну): 0x76 << 1 = 0xEC.
 */
#define BME280_I2C_ADDR         (0x76 << 1)

/* Регістр ідентифікатора чіпа.
 * Завжди містить 0x60 для BME280.
 * Читаємо першим, щоб переконатися, що датчик "живий" і це BME280.
 */
#define BME280_REG_CHIP_ID      0xD0
#define BME280_CHIP_ID_VALUE    0x60

/* Регістр софтверного скидання.
 * Запис значення 0xB6 повертає датчик до заводського стану.
 */
#define BME280_REG_RESET        0xE0
#define BME280_RESET_VALUE      0xB6

/* Регістри керування вимірюванням (Control registers).
 *
 * ctrl_hum (0xF2) — налаштування oversampling для вологості.
 * ctrl_meas (0xF4) — налаштування oversampling для температури та тиску + режим роботи.
 * config (0xF5) — IIR-фільтр, період між вимірюваннями у Normal Mode.
 *
 * ВАЖЛИВО: ctrl_hum треба записувати ПЕРЕД ctrl_meas,
 * бо зміни ctrl_hum набувають чинності тільки після запису ctrl_meas.
 */
#define BME280_REG_CTRL_HUM     0xF2
#define BME280_REG_STATUS       0xF3
#define BME280_REG_CTRL_MEAS    0xF4
#define BME280_REG_CONFIG       0xF5

/* Регістри "сирих" (raw) даних.
 * Тиск:      0xF7 (MSB), 0xF8 (LSB), 0xF9 (XLSB, біти [7:4])  → 20 біт
 * Температура: 0xFA (MSB), 0xFB (LSB), 0xFC (XLSB, біти [7:4])  → 20 біт
 * Вологість:   0xFD (MSB), 0xFE (LSB)                           → 16 біт
 *
 * Зчитуємо всі 8 байт одним burst-read від 0xF7 до 0xFE.
 */
#define BME280_REG_DATA_START   0xF7
#define BME280_DATA_LEN         8

/* Адреси калібрувальних регістрів.
 * Група 1: 0x88–0xA1 (26 байт) — калібровка T1-T3 та P1-P9.
 * Група 2: 0xE1–0xE7 (7 байт) — калібровка H1-H6.
 * Ще один байт H1 знаходиться за адресою 0xA1 (перетинається з Групою 1).
 */
#define BME280_REG_CALIB_T_P    0x88    /* Початок Групи 1 */
#define BME280_CALIB_T_P_LEN    26      /* Кількість байт Групи 1 */
#define BME280_REG_CALIB_H1     0xA1    /* Окремий байт dig_H1 */
#define BME280_REG_CALIB_H2     0xE1    /* Початок Групи 2 */
#define BME280_CALIB_H2_LEN     7       /* Кількість байт Групи 2 */

/* ============================================================
 * СТРУКТУРА КАЛІБРУВАЛЬНИХ КОЕФІЦІЄНТІВ
 * ============================================================
 * Кожен конкретний чіп BME280 на заводі проходить калібрування.
 * Результати записуються в незмінну пам'ять (NVM) чіпа.
 * Ці коефіцієнти УНІКАЛЬНІ для кожного екземпляра датчика.
 *
 * Без цих коефіцієнтів "сирі" дані з АЦП безглузді —
 * вони не відповідають реальній температурі, тиску чи вологості.
 *
 * Типи unsigned/signed важливі — деякі коефіцієнти можуть бути
 * від'ємними. Якщо переплутати тип, компенсація даватиме сміття.
 */
typedef struct {
    /* Температура: 3 коефіцієнти */
    uint16_t dig_T1;    /* unsigned 16-bit */
    int16_t  dig_T2;    /* signed 16-bit */
    int16_t  dig_T3;    /* signed 16-bit */

    /* Тиск: 9 коефіцієнтів */
    uint16_t dig_P1;    /* unsigned 16-bit */
    int16_t  dig_P2;    /* signed 16-bit */
    int16_t  dig_P3;
    int16_t  dig_P4;
    int16_t  dig_P5;
    int16_t  dig_P6;
    int16_t  dig_P7;
    int16_t  dig_P8;
    int16_t  dig_P9;

    /* Вологість: 6 коефіцієнтів */
    uint8_t  dig_H1;    /* unsigned 8-bit */
    int16_t  dig_H2;    /* signed 16-bit */
    uint8_t  dig_H3;    /* unsigned 8-bit */
    int16_t  dig_H4;    /* signed 16-bit */
    int16_t  dig_H5;    /* signed 16-bit */
    int8_t   dig_H6;    /* signed 8-bit */
} BME280_CalibData;

/* ============================================================
 * СТРУКТУРА РЕЗУЛЬТАТУ ВИМІРЮВАННЯ
 * ============================================================ */
typedef struct {
    float temperature;  /* Температура в °C */
    float pressure;     /* Тиск в гПа (гектопаскалі) */
    float humidity;     /* Відносна вологість в % */
} BME280_Data;

/* ============================================================
 * ГОЛОВНА СТРУКТУРА ДРАЙВЕРА
 * ============================================================
 * Зберігає все, що потрібно для роботи з датчиком:
 * - вказівник на I2C-порт (щоб знати, через який I2C спілкуватись)
 * - калібрувальні дані (зчитуються один раз при ініціалізації)
 * - t_fine (проміжне значення температури, потрібне для компенсації тиску і вологості)
 */
typedef struct {
    I2C_HandleTypeDef *hi2c;    /* Вказівник на I2C handle (наприклад, &hi2c1) */
    BME280_CalibData calib;     /* Калібрувальні коефіцієнти */
    int32_t t_fine;             /* Проміжне значення з формули температури */
} BME280_Handle;

/* ============================================================
 * ПУБЛІЧНІ ФУНКЦІЇ (API драйвера)
 * ============================================================
 * Ці функції викликаються з main.c або з FreeRTOS-задач.
 * Повертають HAL_OK при успіху або HAL_ERROR при помилці.
 */

/* Ініціалізація: перевірка Chip ID, зчитування калібровки, конфігурація режиму */
HAL_StatusTypeDef BME280_Init(BME280_Handle *dev, I2C_HandleTypeDef *hi2c);

/* Зчитування даних: trigger вимірювання, зчитування "сирих" даних, компенсація */
HAL_StatusTypeDef BME280_ReadAll(BME280_Handle *dev, BME280_Data *data);

#endif /* BME280_H */
